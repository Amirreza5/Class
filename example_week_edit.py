Day_of_the_week = (input('Please enter the number: '))

if Day_of_the_week == 0:
    print('Saturday')
elif Day_of_the_week == 1:
    print('Sunday')
elif Day_of_the_week == 2:
    print('Monday')
elif Day_of_the_week == 3:
    print('Tuesday')
elif Day_of_the_week == 4:
    print('Wednesday')
elif Day_of_the_week == 5:
    print('Thursday')
elif Day_of_the_week == 6:
    print ('Friday')
else:
    print('Error , choose between 1 to 6')