Day_of_the_week = int(input('Please enter the number: '))

if Day_of_the_week == 0:
    print('Saturday')
if Day_of_the_week == 1:
    print('Sunday')
if Day_of_the_week == 2:
    print('Monday')
if Day_of_the_week == 3:
    print('Tuesday')
if Day_of_the_week == 4:
    print('Wednesday')
if Day_of_the_week == 5:
    print('Thursday')
if Day_of_the_week == 6:
    print ('Friday')
if Day_of_the_week > 6:
    print('Error , choose between 1 to 6')
else:
    print('Error , choose between 1 to 6')