from turtle import Turtle, done

T = Turtle()

T.pencolor('#1A237E')


T.forward(200)
T.left(120)
T.forward(100)
T.left(60)
T.forward(100)
T.left(60)
T.forward(100)


T.penup()
T.goto(200 , 300)
T.pendown()

T.left(120)
T.forward(200)
T.left(120)
T.forward(100)
T.left(60)
T.forward(100)
T.left(60)
T.forward(100)

done()


